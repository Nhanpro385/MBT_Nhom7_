function update_food() {
    let allowedFileTypes = /^image\//; // Kiểm tra loại tệp
    let allowedFileSize = 2048; // Kích thước tệp tối đa (KB)

    let fileInput = document.getElementById('update_item_img');
    let files = fileInput.files;

    // Check if there is a file selected
    if (files.length > 0) {
        let file = files[0]; // Assuming only one file is selected

        if (!file.type.match(allowedFileTypes)) {
            // Kiểm tra loại tệp
            $("#update_item_img").html('<p class="error">File extension error! Please select the allowed file type only.</p>');
            return;
        } else if (file.size > (allowedFileSize * 1024)) {
            // Kiểm tra kích thước tệp (đổi kích thước thành byte)
            $("#update_item_img").html('<p class="error">File size error! Sorry, the selected file size is larger than the allowed size (' + allowedFileSize + 'KB).</p>');
            return;
        }

        // Nếu tất cả kiểm tra đều đúng, thêm tệp vào FormData
        let data = new FormData();
        data.append('update_item_img', file, file.name);
        data.append('update_item_name', $("#update_item_name").val());
        data.append('update_item_price', $("#update_item_price").val());
        data.append('update_item_id', $("#update_item_id").val());
        let xhr = new XMLHttpRequest();
        xhr.open('POST', 'controller/food.php');
        xhr.send(data);

        xhr.onload = function () {
            // Nhận phản hồi và hiển thị trạng thái tải lên
            var response = JSON.parse(xhr.responseText);
            if (xhr.status === 200 && response.status == 'ok') {
                $("#update_item_img").html('<p class="success">File has been uploaded successfully. Click to upload another file.</p>');
            } else if (response.status == 'type_err') {
                $("#update_item_img").html('<p class="error">File extension error! Click to upload another file.</p>');
            } else {
                $("#update_item_img").html('<p class="error">Something went wrong, please try again.</p>');
            }
        };
    } else {
        // No file selected, proceed with other form data
        let data = new FormData();
        data.append('update_item_name', $("#update_item_name").val());
        data.append('update_item_price', $("#update_item_price").val());
data.append('update_item_id', $("#update_item_id").val());
